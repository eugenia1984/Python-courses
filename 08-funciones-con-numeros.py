#FUNCIONES CON NÚMEROS

# Primer ejemplo de función: suma de dos variables(números):
def suma(a, b):             #Definimos la función
  print(a + b)

suma(2, 3)     #Mandamos a llamar a la función
suma(4, 1)     #Podemos reutilizar la función con otros valores de variables
suma(8, 12)

# Segundo ejemple de función, resta de dos nvariables(números):
def resta(a, b):
  print(a - b)

resta(10, 8)

# Ejercicio de práctica, realizar una función para una multiplicación y agregar valores:
def multiplica(c, d):
  print(c * d)

multiplica(10, 2)
multiplica(3, 2)

# Ejercicio de práctica, realizar uan división y agregar valores:
def divide(e, f):
  print(e / f)

divide(10, 5)
divide(130, 3)

